package dk.aau.models.patient; 

public class TidligereSygdomme{
    private String hjerte = ""; 
    private String astma = ""; 
    private String nyre = ""; 
    private String sukkerSyge ="";

    public String getHjerte() {
        return hjerte;
    }

    public void setHjerte(String hjerte) {
        this.hjerte = hjerte;
    }

    public String getAstma() {
        return astma;
    }

    public void setAstma(String astma) {
        this.astma = astma;
    }

    public String getNyre() {
        return nyre;
    }

    public void setNyre(String nyre) {
        this.nyre = nyre;
    }

    public String getSukkerSyge() {
        return sukkerSyge;
    }

    public void setSukkerSyge(String sukkerSyge) {
        this.sukkerSyge = sukkerSyge;
    }


}